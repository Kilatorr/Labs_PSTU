#include <iostream>
using namespace std;
int main() {
    float a;
    cin >> a;
    if (a < 5) {
        a = a * 3; // исправлено: нужно присвоить результат обратно переменной
        cout << a;
    }
    else if (a >= 5 && a <= 7) { // исправлено условие: a <= 7, а не a > 7
        a = a / 10; // исправлено: нужно присвоить результат обратно переменной
        cout << a;
    }
    else if (a > 7) {
        a = a + 3; // исправлено: нужно присвоить результат обратно переменной
        cout << a;
    }
    return 0;
}